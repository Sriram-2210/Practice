package Overview;
public class codeblocks {
    public static void main(String[] args) {
        int x , y=20;
        // the target of this loop is a block
        for(x=0; x<10; x+=1){
            System.out.println("The value of x is " + x);
            System.out.println("The value of y is " + y);
            y-=2;
        } 
    }
}
